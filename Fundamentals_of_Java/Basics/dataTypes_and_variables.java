package Fundamentals_of_Java.Basics;

import java.util.Date;

public class dataTypes_and_variables {
    public static void main(String[] args) {
        //primitive data types
        byte a = 1;
        short b = 12;
        int c = 352;
        float d = 34.5f;
        double e = 12.4;
        char f = 'a';
        boolean g = true;

        //non primitive data type
        String name = "Rohan";
        Date date = new Date();
    }
}
